/* 
Ejercicio 7

Tengo un circulo con el diametro de 18 cm.

Calcula el radio del circulo y dime cual es en una alerta.

*/

const diametroCirculo = 18; // Const porque el diametro no cambiara
let radioCirculo = diametroCirculo / 2;

alert(
  "El radio de un circulo cuyo diametro es " +
    diametroCirculo +
    " cm, es de: " +
    radioCirculo +
    " cm de area"
);
