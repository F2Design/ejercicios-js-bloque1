/* 
Ejercicio 9

Una milla son 1.6 kilometros.

Cuantos kilometros tiene una autopista de 120 millas?

*/

const millasAKilometros = 1.6;
let millas = 120;

let totalKilometros = millasAKilometros * millas;

console.log(
    "Una autopista de " + millas + " millas tiene " + totalKilometros + " kilometros"
);
